/*
 * The expanded private key contains:
 *  - The B0 matrix (four elements)
 *  - The ffLDL tree
 */

size_t skoff_b00(unsigned logn)
{
	(void)logn;
	return 0;
}

size_t skoff_b01(unsigned logn)
{
	return MKN(logn);
}


size_t skoff_b10(unsigned logn)
{
	return 2 * MKN(logn);
}

size_t skoff_b11(unsigned logn)
{
	return 3 * MKN(logn);
}

